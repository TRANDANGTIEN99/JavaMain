package Practice;

public class bai7_CacPhepToan {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;
        System.out.println("a="+a);
        System.out.println("b="+b);


        // cong
        System.out.println("a+b="+(a+b));
        //or
        int kq = a+b;
        System.out.println("a+b="+kq);
        // tru
        System.out.println("a-b="+(a-b));
        //or
        int kq1 = a-b;
        System.out.println("a-b="+kq1);
        System.out.println("a*b="+(a*b));
        System.out.println("a%b="+(a%b));// chia lay du
        System.out.println("a/b="+(a/b));// chia khong lay du
        System.out.println("a/b="+((double)a/b));// phep chia dung nhat

    }
}
